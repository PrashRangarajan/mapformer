#!/bin/bash
# Three post-pipeline analyses to wrap up the current results push:
#   1. Finish TEM single-env CLEAN eval (s1, s2) → update TEM_BACKGROUND_BASELINES.md
#   2. Goal-distance probe on single-env lm200 ckpts (seed-0 of all variants first)
#   3. Cross-scale TEM diagnostic (analytical text — no training)
#
# Foreground, both GPUs free at script start.
set -u
cd /home/prashr
REPO=/home/prashr/mapformer
LOGS=$REPO/logs
mkdir -p "$LOGS" paper_figures

# ============================================================
# (1) Single-env CLEAN eval for TEMFaithful s{0,1,2}
# ============================================================
echo "[$(date)] [post] (1) TEMFaithful single-env clean eval"
JSON_DIR=$REPO/paper_figures/single_env_eval
mkdir -p "$JSON_DIR"
for seed in 0 1 2; do
    CKPT="$REPO/runs/TEMFaithful_clean/seed${seed}/TEMFaithful.pt"
    if [ ! -f "$CKPT" ]; then
        echo "  skip s${seed}: no ckpt"; continue
    fi
    CUDA_VISIBLE_DEVICES=0 python3 -m mapformer.eval_single_env \
        --variant TEMFaithful --checkpoint "$CKPT" \
        > "$JSON_DIR/TEMFaithful_clean_s${seed}.json" \
        2> "$LOGS/tem_clean_eval_s${seed}.err"
done

# Regenerate TEM_BACKGROUND_BASELINES.md with the single-env row populated
python3 -u <<'PYEOF' > "$REPO/TEM_BACKGROUND_BASELINES.md" 2>"$LOGS/tem_bg_regen.err"
