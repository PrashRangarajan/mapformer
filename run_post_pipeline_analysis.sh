# Why does TEMFaithful dominate cross-scale, especially at small grids?

From `TEM_NOVEL_ENV_RESULTS.md`, cross-scale held-out T=512 OOD (n=3):

| Variant                  | size 32       | size 64       | size 128      |
|---|---|---|---|
| **Level15GSF_NoDrop_K16** | 0.826 ± 0.109 | 0.951 ± 0.031 | 0.973 ± 0.019 |
| **TEMFaithful**           | **0.936 ± 0.021** | **0.973 ± 0.006** | **0.981 ± 0.005** |

TEM beats GSF by **+11pp at size 32**, with much tighter seed variance
(0.021 vs 0.109). The advantage shrinks but persists at sizes 64 and 128.

## Mechanistic hypotheses

1. **TEM's W_a is scale-invariant by construction.** `W_a = exp(skew(A_a))`
   is an orthogonal rotation in d_g space. The rotation magnitude is
   learned per discrete action and is the same regardless of grid size.
   At size 32, one "step" covers 1/32 of the torus; at size 128, 1/128.
   TEM's W_a doesn't have to encode this scale — the agent's path is
   just whatever sequence of rotations W_a produces, and memory binds
   (g, x) at each cell.

2. **Level15's PathIntegrator has scale-coupled ω.** The learnable angular
   velocities ω are initialised to span a frequency range tied to
   `grid_size` (see `model.py::PathIntegrator.__init__`):

   ```
   ω_i = ω_max · (1/Δ_max)^(i/(n_b-1))
   ```

   At mixed scales 32 / 64 / 128, the model learns a SHARED ω that has
   to compromise across all three scales. Size 32 is the worst-served
   because its optimal ω range differs most from the average. This
   would predict the seed-level variance pattern too (high std at 32 =
   model didn't converge to a useful ω compromise on some seeds).

3. **TEM's Hopfield retrieval is scale-agnostic.** Memory is keyed by
   `g`, not by spatial coordinates. Retrieval cost / quality doesn't
   depend on how many distinct cells fit on the grid. Level15 attention,
   in contrast, has to learn to attend across longer sequences at
   smaller grids (more revisits per unit length → more candidate keys).

4. **GSF mode initialisation may not help at the small-scale regime.**
   K=16 modes spread `θ_init` offsets across SO(2). At size 32, the
   useful angular resolution per cell is `2π / 32` per step — coarser
   than mode separation. The modes may collapse or fail to capture the
   small-scale structure.

## Falsifiable predictions

- (1) predicts: if we train Level15 with **per-scale ω heads** (one ω
  per scale, gated by the env-size prefix token), the cross-scale gap
  closes. **Cheapest test.**
- (2) predicts: at size 16 (smaller still), the gap should widen further
  for Level15; TEM should hold up. Quick test: retrain on sizes
  {16, 32, 64} instead of {32, 64, 128}.
- (3) predicts: at size 256 (larger still, more cells → harder
  attention regime), the gap should also widen. Less cheap; needs
  bigger compute.
- (4) predicts: K=4 GSF should match K=16 at size 32 (modes weren't
  helping anyway); K=64 should NOT help at size 32 either. Already
  partly verified in MODEOMEGA_FOLLOWUP_RESULTS.md (K-scale is largely
  a wash on torus).

## Workshop takeaway

If we want to defend "MapFormer + correction matches TEM," we need to
address the cross-scale gap honestly. Three honest framings:

1. **"TEM scales better at small grids; we hypothesise this is the
   coupled-ω constraint and propose per-scale ω as a fix."** Frames
   the gap as a known limitation with a proposed remedy.

2. **"At the workshop-relevant scale (size 64+), TEM and Level1.5 are
   tied within seed std."** Restricts the claim to the regimes where
   it actually holds.

3. **"TEM's structural memory is more scale-invariant; Level1.5
   matches on novel envs but cedes at extreme small scales."** Honest
   summary, accepts the gap as a real architectural difference.

Recommend a mix of (1) + (3): claim parity at workshop-relevant scales,
flag the small-scale gap, propose the architectural fix as future work.

