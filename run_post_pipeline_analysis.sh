from pathlib import Path

def fmt(arr):
    if not arr: return "—"
    return f"{np.mean(arr):.3f} ± {np.std(arr):.3f} (n={len(arr)})"

print("# TEMFaithful background baselines (clean, no landmarks)\n")
print("Fills two TEM-shaped gaps in the comparison table.\n")

# --- single-env clean ---
print("## Single-env CLEAN (n_landmarks=0)\n")
print("Single env (seed=0), evaluated on held-out random trajectories.\n")
print("| Variant | T=128 acc | T=128 NLL | T=512 OOD acc | T=512 OOD NLL |")
print("|---|---|---|---|---|")
a128, n128, a512, n512 = [], [], [], []
for s in [0, 1, 2]:
    p = Path(f"paper_figures/single_env_eval/TEMFaithful_clean_s{s}.json")
    if not p.exists(): continue
    j = json.loads(p.read_text())
    if j.get("acc_T128") is not None: a128.append(j["acc_T128"])
    if j.get("nll_T128") is not None: n128.append(j["nll_T128"])
    if j.get("acc_T512") is not None: a512.append(j["acc_T512"])
    if j.get("nll_T512") is not None: n512.append(j["nll_T512"])
print(f"| **TEMFaithful** | {fmt(a128)} | {fmt(n128)} | {fmt(a512)} | {fmt(n512)} |\n")

# --- multi-env clean ---
print("## Multi-env held-out CLEAN (n_landmarks=0)\n")
print("| Variant | Train | Held T=128 | Held T=512 OOD |")
print("|---|---|---|---|")
tr, t128, t512 = [], [], []
for s in [0, 1, 2]:
    ckpt = Path(f"mapformer/runs/TEMFaithful_multienv_clean/seed{s}/TEMFaithful_multienv.pt")
    if not ckpt.exists(): continue
    c = torch.load(ckpt, map_location="cpu", weights_only=False)
    if c.get("train_acc") is not None: tr.append(c["train_acc"])
    if c.get("test_acc") is not None: t128.append(c["test_acc"])
    if c.get("test_acc_T2") is not None: t512.append(c["test_acc_T2"])
print(f"| **TEMFaithful** | {fmt(tr)} | {fmt(t128)} | {fmt(t512)} |\n")

print("*Auto-generated by run_post_pipeline_analysis.sh*")
PYEOF

# ============================================================
# (2) Goal-distance probe — seed-0 across variants first
#     (per feedback_seed_ordering: lands full low-confidence table fastest)
# ============================================================
echo "[$(date)] [post] (2) Goal-distance probe (seed-0 sweep)"
PROBE_DIR=$REPO/paper_figures/goal_distance_probe
mkdir -p "$PROBE_DIR"

probe_one() {
    local variant=$1 ckpt_path=$2 gpu=$3
    local out="$PROBE_DIR/${variant}_lm200_s0.json"
    CUDA_VISIBLE_DEVICES=$gpu python3 -u -m mapformer.probe_goal_distance \
        --variant "$variant" --checkpoint "$ckpt_path" \
        --n-train-envs 1 --n-test-envs 0 \
        --train-env-seed-base 0 \
        --n-trajectories-per-env 200 \
        --T 128 --n-landmarks 200 \
        --train-goal-frac 0.75 \
        --epochs 20 --batch-size 512 \
        --output-json "$out" \
        > "$LOGS/probe_${variant}_lm200_s0.log" 2>&1
}

# Run on two GPUs in pairs
probe_one Vanilla   "$REPO/runs/Vanilla_lm200/seed0/Vanilla.pt"           0 & P1=$!
probe_one Level15   "$REPO/runs/Level15_lm200/seed0/Level15.pt"           1 & P2=$!
wait $P1 $P2
probe_one RoPE        "$REPO/runs/RoPE_lm200/seed0/RoPE.pt"               0 & P1=$!
probe_one TEMFaithful "$REPO/runs/TEMFaithful_lm200/seed0/TEMFaithful.pt" 1 & P2=$!
wait $P1 $P2

# Aggregate
python3 -u <<'PYEOF' > "$REPO/PROBE_GOAL_DISTANCE.md" 2>"$LOGS/probe_aggregate.err"
import json
