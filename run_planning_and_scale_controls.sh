B: Level15 trained at grid sizes 16 and 32 single-size (no multi-scale
   mixing). Tests the coupled-ω hypothesis from TEM_CROSSSCALE_DIAGNOSTIC.
   Also adds --grid-size arg to train_variant.py (was hardcoded to 64).
" 2>&1 | tail -3
git push origin main 2>&1 | tail -3
echo "[$(date)] [planning+scale] done."
